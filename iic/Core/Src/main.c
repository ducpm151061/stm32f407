#include "delay.h"
#include "sys.h"
#include "lcd.h"
#include "gui.h"
#include "test.h"
#include "usart.h"
#include "led.h"
#include "key.h"
#include "24c02.h"

int main(void)
{
	u8 i;
	u8 key_value;
	NVIC_PriorityGroupConfig(NVIC_PriorityGroup_2); //设置系统中断优先级分组2
	delay_init(168);								//初始化延时函数
	USART1_Init(115200);							//串口1初始化
	LED_Init();										//初始化LED
	LCD_Init();										//液晶屏初始化
	KEY_Init();										//按键初始化
	AT24C02_Init();									//初始化IIC
	IIC_24C02_test();								//IIC test
													//循环测试
	while (1)
	{
		key_value = KEY_Scan(0);
		if (key_value == KEY_UP_PRES) //KEY_UP按下
		{
			Show_24C02_Write(); //写入24C02
		}
		else if (key_value == KEY0_PRES) //KEY0按下
		{
			Show_24C02_Read(); //读取24C02
		}
		if (i % 20 == 0)
		{
			LED0 = !LED0;
			i = 0;
		}
		i++;
		delay_ms(10); //延时100ms
	}
}
